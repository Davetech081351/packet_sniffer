# Subtraction
result_sub = 10 - 4
print("Subtraction: 10 - 4 =", result_sub)

# Multiplicaton
result_mul = 7 * 2
print("Multiplication: 7 * 2 =", result_mul)

# Division
result_div = 15 / 3
